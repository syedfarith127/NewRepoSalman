public class ConditionalStatements {
	public static void main(String[] args) {
		// Nested If
		
		int a = 3;
		// 		1			2	 5
		for (int i = 0; i < a; i++) {
				//	3.1		3.2		3.4
			for (int j = 0; j < a; j++) {
				
				System.out.println(j);
			}
				//			4
					System.out.println(i);
			
		}

	}
	
}
