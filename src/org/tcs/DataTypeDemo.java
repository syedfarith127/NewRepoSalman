package org.tcs;

public class DataTypeDemo {
	
	public static void main(String[] args) {
		
		byte age = 25;
		System.out.println("My age is "+age);
		
		short atmPin = 5678;
		System.out.println("My atm Pin is "+atmPin);
		
		int empId = 78945612;
		System.out.println("Employee ID is "+empId);
		
		int cal = 789456;
		System.out.println(cal+"100");
		
		long phNo = 9876543219l;
		System.out.println("My phone Number is "+phNo);
		
		float salary = 598756.2999f;
		System.out.println("My (float) Salary is "+salary);
		
		double salary2 = 565864.15648498489;
		System.out.println("My (double) salary is "+salary2);
		
		char gender = 'M';
		System.out.println("My gender is "+gender);
		
		boolean condition = true;
		System.out.println("Condtion is "+condition);
		
		String email = "salman@gmail.com 4151554";
		System.out.println("Email ID is "+email);
		
	}

}
